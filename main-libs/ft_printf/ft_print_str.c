#include "ft_printf.h"

void	ft_print_str(char *s, int *milk)
{
	if (!s)
	{
		ft_putstr_fd("(null)", 1);
		*milk += 6;
	}
	else
	{
		while (*s)
			ft_print_char(*s++, milk);
	}
}
